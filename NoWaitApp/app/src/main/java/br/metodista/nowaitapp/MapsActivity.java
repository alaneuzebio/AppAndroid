import com.google.android.gms.maps.model.LatLng;
import com.google.android.gms.maps.model.MarkerOptions;

import java.text.SimpleDateFormat;

import static android.location.LocationManager.*;

@SuppressWarnings("ResourceType")
public class MapsActivity extends FragmentActivity implements OnMapReadyCallback {

    private GoogleMap mMap;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_maps);
        // Obtain the SupportMapFragment and get notified when the map is ready to be used.
        SupportMapFragment mapFragment = (SupportMapFragment) getSupportFragmentManager()
                .findFragmentById(R.id.map);
        mapFragment.getMapAsync(this);
    }

    @Override
    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;
        // Add a marker in Sydney and move the camera
        LatLng restautante = new LatLng(-23.657212, -46.568534);
        mMap.addMarker(new MarkerOptions().position(restautante).title("Restaurante Cozinha Chef"));
        mMap.moveCamera(CameraUpdateFactory.newLatLng(restautante));
        mMap.animateCamera(CameraUpdateFactory.zoomTo(15));
    }

    public void tempo(View view) {
        Toast.makeText(this, "TEMPO DE ESPERA CONFIRMADO", Toast.LENGTH_LONG).show();

        startActivity(new Intent(this, PagamentoActivity.class));

    }
}

